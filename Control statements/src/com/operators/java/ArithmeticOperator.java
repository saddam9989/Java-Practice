package com.operators.java;

public class ArithmeticOperator {

	public static void main(String[] args) {
		
		int a=10;
		int b=20;
      
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(b/a);
		System.out.println(a*b);
		System.out.println(b%a);
		System.out.println(a++);
		System.out.println(++a);
		System.out.println(b--);
		System.out.println(--b);
	}

}
