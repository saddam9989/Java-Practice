
public class AssignmentOperator {

	public static void main(String[] args) {
		
		int a = 10;
		
		int b = 20;
		
		
		a = a+b;     // normal process
		
		System.out.println(a);
		
		
		a += b;   // a= a+b;    assignment operators
		
		System.out.println(a);
		
		
		a -= b;   // a= a-b;
		
		System.out.println(a);
		

	}

}
